import java.util.*;
public class Team1 {
		private String name;
		private double att;
		private double mid;
		private double def;
		
		
	public Team1(String n, double a, double m, double d)
	{
		name = n;
		att = a;
		mid = m;
		def = d;
		
		
		
	}
	
	public String getName() {
		return name;
	}
			public double getAtt() {
				return att;
			}
			public double getMid() {
				return mid;
			}
			public double getDef() {
				return def;
			}
		
		
}